
public class User {
	
	int id;
	String username;
	String surname;
	int houseNum;
	String postcode;
	String city;
	String role;
	
	
	User(int id, String username, String surname, int houseNum, String postcode, String city, String role) {
		this.id = id;
		this.username = username;
		this.surname = surname;
		this.houseNum = houseNum;
		this.postcode = postcode;
		this.city = city;
		this.role = role;
				
	}
}
